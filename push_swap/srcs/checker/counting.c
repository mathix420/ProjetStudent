#include "checker.h"

int		count_param(int c, char **v)
{
	int		i;
	int		nb;

	i = 1;
	nb = 0;
	while (v[i] && i < c)
		if (ft_isnum(v[i++]))
			nb += 1;
	return (nb);
}
