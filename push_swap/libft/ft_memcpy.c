#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *restrict)dst)[i] = ((char *restrict)src)[i];
		i++;
	}
	return (dst);
}
