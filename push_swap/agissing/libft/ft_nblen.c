#include "libft.h"

int		ft_nblen(int n)
{
	int				i;
	unsigned int	nb;

	i = (n < 0 ? 1 : 0);
	nb = (n < 0 ? -n : n);
	while (nb > 10)
	{
		nb /= 10;
		i++;
	}
	return (i + 1);
}
