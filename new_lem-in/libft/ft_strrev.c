#include "libft.h"

char	*ft_strrev(char *s)
{
	char	tmp;
	int		i;
	int		j;

	j = 0;
	i = ft_strlen(s) - 1;
	while (i > j)
	{
		tmp = s[j];
		s[j] = s[i];
		s[i] = tmp;
		i--;
		j++;
	}
	return (s);
}
