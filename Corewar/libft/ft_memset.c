#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	void			*pt;

	pt = s;
	i = 0;
	while (i < n)
	{
		*(unsigned char*)s = c;
		s++;
		i++;
	}
	return (pt);
}
